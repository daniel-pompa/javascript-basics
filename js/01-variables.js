/* Variable rules
- Can't start with numbers or special characters. Underscore if allowed.
- Can't have spaces
- JavaScript recommends the use of camelCase */

// Variables - let
let customer = 'Emma';
let age = 40;
let logicalData = false;

// Can be declared without initialized
let email;

console.log(customer);

// Reassignment of variables
customer = 'Daniel';
age = 48;
logicalData = true;
email = 'daniel.pompa@gmail.com';

console.log(customer);
console.log(age);
console.log(email);
console.log(logicalData);
